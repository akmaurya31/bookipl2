<script>
    document.addEventListener("DOMContentLoaded", function() {
        const buttons = document.querySelectorAll(".ticket-type-button");
        const totalPriceElement = document.querySelector(".text-lg.font-bold.text-red-600");
        const perPriceElement = document.querySelector(".text-sm.perprice.font-medium");
        const quantityBtns = document.querySelectorAll(".quantity-btn");
        const quantityDisplay = document.querySelector(".w-10.text-center");
        let quantity = 1;
        let selectedPrice = 599; // Default price
        let selectedStand = "General Stand"; // Default stand name

        // Function to clean and parse price correctly
        function parsePrice(priceText) {
            return parseFloat(priceText.replace(/[^0-9]/g, ""));
        }

        // Function to update UI and localStorage
        function updatePrice() {
            const totalPrice = selectedPrice * quantity;
            localStorage.setItem("selectedTicketPrice", selectedPrice);
            localStorage.setItem("selectedTicketQuantity", quantity);
            localStorage.setItem("selectedTotalPrice", totalPrice);
            localStorage.setItem("selectedStand", selectedStand);
            totalPriceElement.textContent = `₹${totalPrice.toLocaleString("en-IN")}`;
            perPriceElement.textContent = `Price per Ticket: ₹${selectedPrice.toLocaleString("en-IN")}`;
        }

        if (buttons.length > 0) {
            // Set the first button as selected by default
            buttons[0].classList.add("border-red-600", "bg-red-50");
            selectedPrice = parsePrice(buttons[0].querySelector(".font-bold").textContent) || 599;
            selectedStand = buttons[0].querySelector("h3").textContent.trim(); // Extract stand name
            updatePrice();
        }

        buttons.forEach(button => {
            button.addEventListener("click", function() {
                // Remove styles from all buttons
                buttons.forEach(btn => btn.classList.remove("border-red-600", "bg-red-50", "bg-white"));

                // Add styles to the selected button
                this.classList.add("border-red-600", "bg-red-50");

                // Get the selected ticket price
                selectedPrice = parsePrice(this.querySelector(".font-bold").textContent) || 599;
                selectedStand = this.querySelector("h3").textContent.trim(); // Get stand name

                // Reset quantity to 1
                quantity = 1;
                quantityDisplay.textContent = quantity;
                document.querySelector(".quantity-btn:first-child").setAttribute("disabled", "true");

                updatePrice();
            });
        });

        // Handle quantity buttons
        quantityBtns.forEach(btn => {
            btn.addEventListener("click", function() {
                const isIncrement = this.querySelector("i").classList.contains("fa-plus");

                if (isIncrement) {
                    quantity++;
                } else if (quantity > 1) {
                    quantity--;
                }

                // Update UI
                quantityDisplay.textContent = quantity;
                updatePrice();


                document.querySelector(".quantity-btn:first-child").disabled = quantity <= 1;
            });
        });


        const storedPrice = localStorage.getItem("selectedTicketPrice");
        const storedQuantity = localStorage.getItem("selectedTicketQuantity");
        const storedStand = localStorage.getItem("selectedStand");

        if (storedPrice && storedQuantity && storedStand) {
            selectedPrice = parseFloat(storedPrice) || 599;
            quantity = parseInt(storedQuantity) || 1;
            selectedStand = storedStand;
            quantityDisplay.textContent = quantity;
            updatePrice();
        }
    });
